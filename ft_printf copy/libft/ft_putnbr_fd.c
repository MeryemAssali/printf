#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nt;

	nt = n;
	if (nt < 0)
	{
		write(fd, "-", 1);
		nt *= -1;
	}
	if (nt > 9)
		ft_putnbr_fd(nt / 10, fd);
	nt = (nt % 10) + '0';
	write (fd, &nt, 1);
}
